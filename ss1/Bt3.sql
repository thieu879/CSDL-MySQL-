-- ba đặc điểm chính của MySQL:
-- 1, Hiệu xuất cao và đáng tin cậy: MySQL có khả năng sử khối lương lý dữ liệu lớn và có tốc độ cao.
-- 2, Miễn phí.
-- 3, Bảo mật tốt: quản lý quyền truy cập, mã hoá dữ liệu, bảo vệ truy cập từ xa.

-- ba trường hợp thực tế mà MySQL có thể được sử dụng:
-- 1, Quản lý trang thương mại điện tử: lưu trữ thông tin đơn hàng, khách hàng,...
-- 2, Quản lý học sinh trong trường học: lưu trữ thông tin học sinh, điểm số, lớp học,...
-- 3, Quản lý ứng dụng mạng xã hội: lưu trữ thông tin người dùng, mối quan hệ, bài viết,...  